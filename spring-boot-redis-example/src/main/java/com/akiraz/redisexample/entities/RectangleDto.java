package com.akiraz.redisexample.entities;

public class RectangleDto {

	private Coordinate top,bottom,left,right;

	public Coordinate getTop() {
		return top;
	}

	public void setTop(Coordinate top) {
		this.top = top;
	}

	public Coordinate getBottom() {
		return bottom;
	}

	public void setBottom(Coordinate bottom) {
		this.bottom = bottom;
	}

	public Coordinate getLeft() {
		return left;
	}

	public void setLeft(Coordinate left) {
		this.left = left;
	}

	public Coordinate getRight() {
		return right;
	}

	public void setRight(Coordinate right) {
		this.right = right;
	}
}
